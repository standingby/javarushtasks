package com.javarush.task.task04.task0438;

/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        //напишите тут ваш код
        for (int i = 1; i <= 10; i++) {
            if (i == 1) {
                for (int k = 1; k <= 10; k++) {
                    System.out.print(8);
                }
            }
            System.out.println(8);
        }
    }
}
